        <p><strong>Đối tượng được sử dụng</strong>: Là thứ mà khách hàng sử dụng, trải nghiệm, thường sẽ tồn tại sau quá trình sử dụng dịch vụ và được bán đi bán lại nhiều lần cho nhiều khách khác nhau.</p>
        <p>Tuy dịch vụ đem lại sự trải nghiệm chủ yếu về tinh thần, và dịch vụ cũng kết thúc khi khách hàng kết thúc quá trình trải nghiệm, nhưng các đối tượng được trải nghiệm có thể vẫn còn tồn tại, ví dụ Phòng khách sạn.</p>
        <p><strong>Địa điểm sử dụng</strong>: Là một không gian cố định tại đó khách hàng sử dụng dịch vụ, ví dụ khách sạn, nhà hàng, con tàu, v.v</p>
        <p><strong>Nhà cung cấp</strong>: Là đơn vị hay cá nhân làm ra và bán dịch vụ</p>
